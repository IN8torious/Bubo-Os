// в”Җв”Җ Kernel main в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// In 64-bit: rdi = multiboot2 magic, rsi = multiboot2 info pointer
void kernel_main(uint64_t multiboot_magic, uint64_t multiboot_info_addr) {

    // в”Җв”Җ Layer 0: VGA terminal (text mode fallback) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    terminal_init();
    draw_banner();

    uint8_t white = vga_entry_color(VGA_WHITE,       VGA_BLACK);
    uint8_t green = vga_entry_color(VGA_LIGHT_GREEN, VGA_BLACK);
    uint8_t red   = vga_entry_color(VGA_LIGHT_RED,   VGA_BLACK);
    (void)white; (void)green; (void)red;

    // Verify multiboot2
    terminal_setcolor(white);
    terminal_write("  [ BOOT ] Multiboot2: ");
    if (multiboot_magic == MULTIBOOT2_BOOTLOADER_MAGIC) {
        terminal_setcolor(green);
        terminal_writeline("VALID  вң“");
    } else {
        terminal_setcolor(red);
        terminal_writeline("INVALID вҖ” continuing anyway");
    }

    // в”Җв”Җ Layer 2: Physical Memory Manager в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    pmm_init((uint32_t)multiboot_info_addr);

    // в”Җв”Җ Layer 3: Interrupt Descriptor Table в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    idt_init();

    // в”Җв”Җ Layer 3: Virtual Memory Manager + kmalloc heap в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    vmm_init();

    // в”Җв”Җ Layer 3.5: Framebuffer вҖ” VESA pixel display в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    bool fb_ok = fb_init_from_multiboot((uint32_t)multiboot_info_addr);
    if (!fb_ok) fb_init_fallback();

    // Draw Akatsuki boot splash screen
    corvus_draw_boot_screen();
    corvus_draw_boot_progress(20, "Memory subsystems initialized...");

    // в”Җв”Җ Layer 4: PIT Timer (100Hz вҖ” drives CORVUS cognitive loop) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    pit_init(100);
    corvus_draw_boot_progress(40, "PIT timer running at 100Hz...");

    // в”Җв”Җ Layer 4: Keyboard (CORVUS perception module) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    keyboard_init();
    corvus_draw_boot_progress(55, "Keyboard perception module online...");

    // в”Җв”Җ Layer 4: Scheduler в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    scheduler_init();
    corvus_draw_boot_progress(70, "Scheduler initialized...");

    // в”Җв”Җ Layer 5: CORVUS Orchestration Engine в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    corvus_init();
    corvus_draw_boot_progress(90, "CORVUS agents activated...");

    // в”Җв”Җ Boot complete в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    terminal_setcolor(red);
    terminal_writeline("  в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ");
    terminal_setcolor(green);
    terminal_writeline("  Raven AOS boot sequence complete вҖ” all systems operational");
    terminal_setcolor(red);
    terminal_writeline("  в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ");

    corvus_draw_boot_progress(100, "Boot complete. Launching CORVUS dashboard...");

    // Brief pause so user can see the boot screen
    for (volatile int i = 0; i < 80000000; i++) {}

    // в”Җв”Җ Layer 6: Switch to graphical CORVUS dashboard в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    corvus_draw_dashboard();

    // в”Җв”Җ Layer 7: CORVUS Shell (runs on top of the dashboard) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    shell_init();

    // в”Җв”Җ Main kernel loop вҖ” wait for interrupts в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    // The PIT drives CORVUS, the keyboard drives the shell
    // Everything is interrupt-driven from here
    while (1) {
        __asm__ volatile("hlt");
    }
}
