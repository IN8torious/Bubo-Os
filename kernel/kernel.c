#define MULTIBOOT2_BOOTLOADER_MAGIC 0x36d76289

// в”Җв”Җ Draw the Raven OS boot banner (VGA text fallback) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
